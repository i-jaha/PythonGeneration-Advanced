n = int(input().strip())
infected_indices = []

for i in range(n):
    s = input().strip()
    pattern = "anton"
    index = 0
    
    for char in s:
        if index < len(pattern) and char == pattern[index]:
            index += 1
        if index == len(pattern):
            infected_indices.append(i + 1)
            break

print(*infected_indices)