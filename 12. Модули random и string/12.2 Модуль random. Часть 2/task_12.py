import random
n = int(input())
names = [input() for _ in range(n)]
friends = names.copy()
while True:
    random.shuffle(friends)
    if all(names[i] != friends[i] for i in range(n)):
        break
for student, friend in zip(names, friends):
    print(student, '-', friend)