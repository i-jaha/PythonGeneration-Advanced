n = int(input())
sales = {}

for _ in range(n):
    buyer, product, count = input().split()
    count = int(count)
    sales.setdefault(buyer, {}).setdefault(product, 0)
    sales[buyer][product] += count

for buyer in sorted(sales):
    print(f"{buyer}:")
    for product in sorted(sales[buyer]):
        print(f"{product} {sales[buyer][product]}")