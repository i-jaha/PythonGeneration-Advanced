n = int(input())
matrix = [[0 for _ in range(n)] for _ in range(n)]

for row in range(n):
    for col in range(n):
        if (row <= col and row <= n - col - 1) or \
            (row >= col and row >= n - col - 1):
            matrix[row][col] = 1

for row in matrix:
    print(*[str(x).ljust(3) for x in row])