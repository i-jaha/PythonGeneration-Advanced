'''–ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ —Å–ø–∏—Ä–∞–ª—å—é üåÄüå∂Ô∏èüå∂Ô∏è
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n –∏ m. 
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —Å–æ–∑–¥–∞–µ—Ç –º–∞—Ç—Ä–∏—Ü—É —Ä–∞–∑–º–µ—Ä–æ–º n√óm, 
–∑–∞–ø–æ–ª–Ω–∏–≤ –µ–µ "—Å–ø–∏—Ä–∞–ª—å—é" –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –æ–±—Ä–∞–∑—Ü–æ–º.

–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –Ω–∞ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n –∏ m ‚Äì 
–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ.

–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –º–∞—Ç—Ä–∏—Ü—É –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ –æ–±—Ä–∞–∑—Ü–æ–º.

–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. 
–î–ª—è –≤—ã–≤–æ–¥–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Ç—Ä–∏—Ü—ã –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–∞—Ö 
–æ—Ç–≤–æ–¥–∏—Ç–µ —Ä–æ–≤–Ω–æ 3 —Å–∏–º–≤–æ–ª–∞ –Ω–∞ –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç. 
–î–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust(). 
–ú–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ ljust(), —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–∏–º–µ—Ç –∏ —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ. üòá
'''

nums = [int(i) for i in input().split()]
n, m = nums[0], nums[1]
matrix = [[0 for _ in range(m)] for _ in range(n)]
num = 1
top, bottom = 0, n - 1
left, right = 0, m - 1

while num <= n * m:
    # –°–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ
    for j in range(left, right + 1):
        matrix[top][j] = num
        num += 1
    top += 1

    # –°–≤–µ—Ä—Ö—É –≤–Ω–∏–∑
    for i in range(top, bottom + 1):
        matrix[i][right] = num
        num += 1
    right -= 1

    # –°–ø—Ä–∞–≤–∞ –Ω–∞–ª–µ–≤–æ
    if top <= bottom:
        for j in range(right, left - 1, -1):
            matrix[bottom][j] = num
            num += 1
        bottom -= 1

    # –°–Ω–∏–∑—É –≤–≤–µ—Ä—Ö
    if left <= right:
        for i in range(bottom, top - 1, -1):
            matrix[i][left] = num
            num += 1
        left += 1

for row in matrix:
    print(*[str(x).ljust(3) for x in row])