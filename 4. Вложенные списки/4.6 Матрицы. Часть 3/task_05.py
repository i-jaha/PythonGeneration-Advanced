# review 1
n = int(input())
matrix = [[0]*n for _ in range(n)]

for i in range(len(matrix)):
    for j in range(len(matrix)):
        if (i == j) or (i == n - j - 1):
            matrix[i][j] = 1

for row in matrix:
    print(*[str(x).ljust(3) for x in row])




