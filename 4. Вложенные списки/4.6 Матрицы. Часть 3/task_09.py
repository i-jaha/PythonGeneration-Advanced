nums = [int(i) for i in input().split()]
n, m = nums[0], nums[1]
num = 1
matrix = [[0 for _ in range(m)] for _ in range(n)]

for q in range(n + m - 1):
    for i in range(n):
        for j in range(m):
            if i + j == q:
                matrix[i][j] = num
                num += 1

for row in matrix:
    print(*[str(x).ljust(3) for x in row])