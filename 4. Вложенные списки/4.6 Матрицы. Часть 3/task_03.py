nums = [int(i) for i in input().split()]
n, m = nums[0], nums[1]
matrix = [[0] * m for _ in range(n)]

num = 1
for i in range(n):
    for j in range(m):
        matrix[i][j] = num
        num += 1
for row in matrix:
    print(*[str(x).ljust(3) for x in row])