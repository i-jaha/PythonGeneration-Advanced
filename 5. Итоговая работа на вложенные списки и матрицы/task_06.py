n = int(input())
matrix = [[int(i) for i in input().split()] for _ in range(n)]

is_latin = True
required = list(range(1, n + 1))

for row in matrix:
    if sorted(row) != required:
        is_latin = False
        break

if is_latin:
    for j in range(n):
        col = [matrix[i][j] for i in range(n)]
        if sorted(col) != required:
            is_latin = False
            break

print("YES" if is_latin else "NO")