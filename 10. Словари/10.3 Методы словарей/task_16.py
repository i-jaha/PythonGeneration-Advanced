text = input().split()
result = []
counts = {}

for word in text:
    counts[word] = counts.get(word, 0)
    if counts[word] == 0:
        result.append(word)
    else:
        result.append(f"{word}_{counts[word]}")
    counts[word] += 1

print(*result)